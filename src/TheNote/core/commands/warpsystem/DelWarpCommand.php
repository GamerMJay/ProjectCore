namespace TheNote\core\commands\warpsystem;

use pocketmine\command\Command;
use pocketmine\command\CommandSender;
use pocketmine\player\Player;
use TheNote\core\CoreAPI;
use TheNote\core\Main;
use TheNote\core\utils\Permissions;

class DelWarpCommand extends Command
{
    private Main $plugin;

    public function __construct(Main $plugin)
    {
        $this->plugin = $plugin;
        $api = new CoreAPI();
        parent::__construct("delwarp", $api->getCommandPrefix("Prefix") . $api->getCommandPrefix("DelWarpDescription"), "/delwarp <warpname>");
        $this->setPermission(Permissions::$delwarp);
    }

    public function execute(CommandSender $sender, string $commandLabel, array $args): bool
    {
        $api = new CoreAPI();
        if (!$sender instanceof Player) {
            $sender->sendMessage($api->getCommandPrefix("Error") . $api->getCommandPrefix("CommandIngame"));
            return false;
        }
        if (!$this->testPermission($sender)) {
            $sender->sendMessage($api->getCommandPrefix("Error") . $api->getLang($sender->getName(),"NoPermission"));
            return false;
        }
        if (empty($args[0])) {
            $sender->sendMessage($api->getCommandPrefix("Warp") . $api->getLang($sender->getName(),"DelWarpUsage"));
            return false;
        }
        if ($api->getWarpExist($args[0])) {
            $api->setWarpRemove($args[0]);
            $message = str_replace("{warp}", $args[0], $api->getLang($sender->getName(),"DelWarpConfirm"));
            $sender->sendMessage($api->getCommandPrefix("Warp") . $message);
        } else {
            $message = str_replace("{warp}", $args[0], $api->getLang($sender->getName(),"DelWarpNotFound"));
            $sender->sendMessage($api->getCommandPrefix("Warp") . $message);
        }
        return true;
    }
}