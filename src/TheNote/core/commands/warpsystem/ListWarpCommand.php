namespace TheNote\core\commands\warpsystem;

use pocketmine\command\Command;
use pocketmine\command\CommandSender;
use pocketmine\player\Player;
use pocketmine\utils\Config;
use TheNote\core\CoreAPI;
use TheNote\core\Main;
use TheNote\core\utils\Permissions;

class ListWarpCommand extends Command
{
    private Main $plugin;

    public function __construct(Main $plugin)
    {
        $this->plugin = $plugin;
        $api = new CoreAPI();
        parent::__construct("listwarp", $api->getCommandPrefix("Prefix") . $api->getCommandPrefix("ListWarpDescription"), "/listwarp", ["warps"]);
        $this->setPermission(Permissions::$defaultperm);
    }
    public function execute(CommandSender $sender, string $commandLabel, array $args): bool
    {
        $api = new CoreAPI();
        if (!$sender instanceof Player) {
            $sender->sendMessage($api->getCommandPrefix("Error") . $api->getCommandPrefix("CommandIngame"));
            return false;
        }
        $warp = new Config($this->plugin->getDataFolder() . CoreAPI::$cloud . "warps.json", Config::JSON);
        $warps = $warp->getAll(true);
        $sender->sendMessage($api->getCommandPrefix("Warp") . $api->getLang($sender->getName(),"ListWarp"));
        foreach ($warps as $key) {
            if ($warps === null) {
                $sender->sendMessage($api->getCommandPrefix("Warp" . $api->getLang($sender->getName(),"ListWarpError")));
            } else {
                $sender->sendMessage("§e- " . $key);
            }
        }
        return true;
    }
}