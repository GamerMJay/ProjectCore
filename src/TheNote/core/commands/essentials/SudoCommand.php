namespace TheNote\core\commands\essentials;

use pocketmine\player\Player;
use pocketmine\command\Command;
use pocketmine\command\CommandSender;
use TheNote\core\CoreAPI;
use TheNote\core\Main;
use TheNote\core\utils\Permissions;

class SudoCommand extends Command
{
    private Main $plugin;

    public function __construct(Main $plugin)
    {
        $this->plugin = $plugin;
        $api = new CoreAPI();
        parent::__construct("sudo", $api->getCommandPrefix("Prefix") . $api->getCommandPrefix("SudoDescription"), "/sudo");
        $this->setPermission(Permissions::$sudo);
    }

    public function execute(CommandSender $sender, string $commandLabel, array $args): bool
    {
        $api = new CoreAPI();
        if (!$this->testPermission($sender)) {
            $sender->sendMessage($api->getCommandPrefix("Error") . $api->getLang($sender->getName(),"NoPermission"));
            return false;
        }
        if (count($args) < 2) {
            $sender->sendMessage($api->getCommandPrefix("Info") . $api->getLang($sender->getName(),"SudoUsage"));
            return true;
        }
        $player = $api->findPlayer($sender, array_shift($args));
        if ($player instanceof Player) {
            $player->chat(trim(implode(" ", $args)));
            return true;
        } else {
            $sender->sendMessage($api->getCommandPrefix("Error") . $api->getLang($sender->getName(),"PlayernotOnline"));
            return true;
        }
    }
}