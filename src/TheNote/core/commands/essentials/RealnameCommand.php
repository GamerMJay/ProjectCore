namespace TheNote\core\commands\essentials;

use pocketmine\command\Command;
use pocketmine\command\CommandSender;
use pocketmine\player\Player;
use TheNote\core\CoreAPI;
use TheNote\core\Main;
use TheNote\core\utils\Permissions;

class RealnameCommand extends Command
{
    private Main $plugin;

    public function __construct(Main $plugin)
    {
        $this->plugin = $plugin;
        $api = new CoreAPI();
        parent::__construct("realname", $api->getCommandPrefix("prefix") . $api->getCommandPrefix("RealnameDescription"), "/realname");
        $this->setPermission(Permissions::$realname);
    }

    public function execute(CommandSender $sender, string $commandLabel, array $args): bool
    {
        $api = new CoreAPI();
        if (!$sender instanceof Player) {
            $sender->sendMessage($api->getCommandPrefix("error") . $api->getLang($sender->getName(),"CommandIngame"));
            return false;
        }
        if (!$this->testPermission($sender)) {
            $sender->sendMessage($api->getCommandPrefix("error") . $api->getLang($sender->getName(),"NoPermission"));
            return false;
        }
        $target = $api->findPlayer($sender, $args[0]);
        if ($target == null) {
            $sender->sendMessage($api->getCommandPrefix("error") . $api->getLang($sender->getName(),"PlayernotOnline"));
            return false;
        }
        $message = str_replace("{target}" , $sender->getDisplayName(), $api->getLang($sender->getName(),"RealNameSucces"));
        $message1 = str_replace("{targetname}" , (str_ends_with($sender->getName(), "s") ? "'" : "'s"), $message);
        $sender->sendMessage($api->getCommandPrefix("prefix") . $message1);
        return true;
    }

}
