namespace TheNote\core\commands\essentials;

use pocketmine\player\Player;
use pocketmine\command\Command;
use pocketmine\command\CommandSender;
use TheNote\core\CoreAPI;
use TheNote\core\Main;
use TheNote\core\utils\Permissions;

class NightCommand extends Command {

    private Main $plugin;

    public function __construct(Main $plugin)
    {
        $this->plugin = $plugin;
        $api = new CoreAPI();
        parent::__construct("night", $api->getCommandPrefix("Prefix") . $api->getCommandPrefix("NightDescription"), "/night", ["nacht"]);
        $this->setPermission(Permissions::$night);
    }

    public function execute(CommandSender $sender, string $commandLabel, array $args): bool
    {
        $api = new CoreAPI();
        if (!$sender instanceof Player) {
            $sender->sendMessage($api->getCommandPrefix("Error") . $api->getCommandPrefix("CommandIngame"));
            return false;
        }
        if (!$this->testPermission($sender)) {
            $sender->sendMessage($api->getCommandPrefix("Error") . $api->getLang($sender->getName(), "NoPermission"));
            return false;
        }
        $sender->getWorld()->setTime(14000);
        $sender->sendMessage($api->getCommandPrefix("Prefix") . $api->getLang($sender->getName(), "NightSucces"));
        return false;
    }
}