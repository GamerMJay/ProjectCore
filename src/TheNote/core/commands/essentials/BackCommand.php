namespace TheNote\core\commands\essentials;

use pocketmine\command\Command;
use pocketmine\command\CommandSender;
use pocketmine\player\Player;
use pocketmine\world\Position;
use TheNote\core\CoreAPI;
use TheNote\core\Main;
use TheNote\core\utils\Permissions;

class BackCommand extends Command
{
    private $plugin;

    public function __construct(Main $plugin)
    {
        $this->plugin = $plugin;
        $api = new CoreAPI();
        parent::__construct("back", $api->getCommandPrefix("Prefix") . $api->getCommandPrefix("BackDescription"), "/back");
        $this->setPermission(Permissions::$back);
    }

    public function execute(CommandSender $sender, string $commandlabel, array $args): bool
    {
        $api = new CoreAPI();
        if (!$sender instanceof Player) {
            $sender->sendMessage($api->getCommandPrefix("Error") . $api->getCommandPrefix("CommandIngame"));
            return false;
        }
        if (!$this->testPermission($sender)) {
            $sender->sendMessage($api->getCommandPrefix("Error") . $api->getLang($sender->getName(),"NoPermission"));
            return false;
        }
        if ($api->getBackExist($sender->getName())) {
            $pos = explode(" ", $api->getBack($sender->getName()));
            $x = (int)$pos[0];
            $y = (int)$pos[1];
            $z = (int)$pos[2];
            $level = $this->plugin->getServer()->getWorldManager()->getWorldByName($pos[3]);
            $sender->teleport(new Position($x, $y, $z, $level));
            $sender->sendMessage($api->getCommandPrefix("Prefix") . $api->getLang($sender->getName(),"BackMessage"));
        }
        return true;
    }
}