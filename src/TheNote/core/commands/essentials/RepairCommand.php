namespace TheNote\core\commands\essentials;

use pocketmine\item\Armor;
use pocketmine\item\Tool;
use pocketmine\player\Player;
use pocketmine\command\Command;
use pocketmine\command\CommandSender;
use TheNote\core\CoreAPI;
use TheNote\core\Main;
use TheNote\core\utils\Permissions;

class RepairCommand extends Command
{

    private Main $plugin;

    public function __construct(Main $plugin)
    {
        $this->plugin = $plugin;
        $api = new CoreAPI();
        parent::__construct("repair", $api->getCommandPrefix("Prefix") . $api->getCommandPrefix("RepairDescription"), "/repair");
        $this->setPermission(Permissions::$repair);
    }

    public function execute(CommandSender $sender, string $commandLabel, array $args): bool
    {
        $api = new CoreAPI();
        if (!$sender instanceof Player) {
            $sender->sendMessage($api->getCommandPrefix("Error") . $api->getCommandPrefix("CommandIngame"));
            return false;
        }
        if (!$this->testPermission($sender)) {
            $sender->sendMessage($api->getCommandPrefix("Error") . $api->getLang($sender->getName(),"NoPermission"));
            return false;
        }
        $item = $sender->getInventory()->getItem($sender->getInventory()->getHeldItemIndex());
        if ($item->isNull()) {
            $sender->sendMessage($api->getCommandPrefix("Error") . $api->getLang($sender->getName(),"RepairError"));
            return false;
        }

        if(!$item instanceof Tool && !$item instanceof Armor){
            $sender->sendMessage($api->getCommandPrefix("Error") . $api->getLang($sender->getName(),"RepairNoTool"));
            return false;
        }

        $item->setDamage(0);
        $sender->getInventory()->setItemInHand($item);
        $sender->sendMessage($api->getCommandPrefix("Prefix") . $api->getLang($sender->getName(),"RepairSucces"));
        return true;
    }
}