namespace TheNote\core\commands\other;;

use pocketmine\command\Command;
use pocketmine\command\CommandSender;
use pocketmine\player\Player;
use pocketmine\utils\Config;
use TheNote\core\CoreAPI;
use TheNote\core\Main;
use TheNote\core\utils\Permissions;

class SetHubCommand extends Command
{
    private Main $plugin;

    public function __construct(Main $plugin)
    {
        $this->plugin = $plugin;
        $api = new CoreAPI();
        parent::__construct("sethub", $api->getCommandPrefix("Prefix") . $api->getCommandPrefix("SetHubDescription"), "/sethub");
        $this->setPermission(Permissions::$sethub);
    }

    public function execute(CommandSender $sender, string $commandLabel, array $args): bool
    {
        $api = new CoreAPI();
        if (!$sender instanceof Player) {
            $sender->sendMessage($api->getCommandPrefix("Error") . $api->getCommandPrefix("CommandIngame"));
            return false;
        }
        if (!$this->testPermission($sender)) {
            $sender->sendMessage($api->getCommandPrefix("Error") . $api->getLang($sender->getName(),"NoPermission"));
            return false;
        }
        $x = $sender->getLocation()->getX();
        $y = $sender->getLocation()->getY();
        $z = $sender->getLocation()->getZ();
        $world = $sender->getWorld()->getFolderName();

        $warp = new Config($this->plugin->getDataFolder() . CoreAPI::$cloud . "warps.json", Config::JSON);
        $warp->set("hub", ["X" => $x, "Y" => $y, "Z" => $z, "world" => $world]);
        $warp->save();
        $message = str_replace("{x}", $x, $api->getLang($sender->getName(),"SetHubSucces"));
        $message1 = str_replace("{y}", $y , $message);
        $message2 = str_replace("{z}", $z , $message1);
        $message3 = str_replace("{world}", $world, $message2);
        $sender->sendMessage($api->getCommandPrefix("Prefix") . $message3);

        return true;
    }
}